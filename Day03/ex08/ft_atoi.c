int			ft_atoi(char *str)
{
	int result;
	int i;

	i = 0;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
						(str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		str++;
	if (*str == '-' || *str == '+')
		i++;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		result = result * 10 + (str[i] - 48);
		i++;
	}
	if (str[0] == '-')
		result *= -1;
	return (result);
}
