int			ft_antidote(int i, int j, int k)
{
	int arr[3];

	arr[0] = i;
	arr[1] = j;
	arr[2] = k;
	i = 0;
	while (i < 2)
	{
		if (arr[i] > arr[i + 1])
		{
			j = arr[i];
			arr[i] = arr[i + 1];
			arr[i + 1] = j;
			i = -1;
		}
		i++;
	}
	return (arr[1]);
}
