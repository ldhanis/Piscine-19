int			ft_strcmp(char *s1, char *s2)
{
	int comp;

	comp = 0;
	while (*s1 == *s2 && *s1 != '\0' && *s2 != '\0')
	{
		s1++;
		s2++;
	}
	if (*s1 != *s2)
		return (-(*s2 - *s1));
	return (0);
}
