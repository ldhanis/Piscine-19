#!/bin/sh
cat /etc/passwd | grep -v "^#" | sed -n "n;p" | rev | cut -d ":" -f7 | sort -r | head -n $FT_LINE2 | tail -n +$((FT_LINE2-FT_LINE1)) | sed 's/^/, /' | tr -d '\n' | cut  -c3- | rev | cut -c2- | sed 's/././1' | rev
